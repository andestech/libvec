#include "internal_nds_types.h"

/* function description */
int riscv_vec_neg_q7(q7_t * dst, q7_t * src, uint32_t count)
{
    while (count != 0u)
    {
        /* y = -x */
        *dst++ = (q7_t) NDS_ISA_SATS((0 - *src++), 8u);

        count--;
    }
    return NDSV_OK;
}

